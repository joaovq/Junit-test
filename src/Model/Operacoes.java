package Model;

public class Operacoes {
    public Operacoes() {
    }

    public static int soma(int a, int b){
        return a+b;
    }
    public static int multiplicacao(int a,int b){
        return a*b;
    }
    public static double divisao(double a , double b){
        return a/b;
    }
    public static int subtracao(int a , int b){
        return a-b;
    }
}